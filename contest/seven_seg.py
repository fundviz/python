def seven_seg(i):
  dict={'1':['  |','  |'],'2':[' _|','|_ '],'3':[' _|',' _|'],'4':['|_|','  |'],'5':['|_ ',' _|'],'6':['|_ ','|_|'],'7':['  |','  |'],'8':['|_|','|_|'],'9':['|_|',' _|'],'0':['| |','|_|']}
  s=''
  for r in range(3):
    for n in i:
      if r==0 and n in ['1','4']:
        s=s+'   '
      elif r==0 and n not in ['1','4']:
        s=s+' _ '
      else:
        s=s+dict[n][r-1]
    s=s+'\n'
  return s
if __name__=='__main__':
  seven_seg('1234567890')
